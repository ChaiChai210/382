package com.colin.anbet.entity;

public class GameList {
    private String androidImgUrl;
    private String code;
    private String gameKind;
    private String url;
    private String id;
    private String name;
    private String type;
    private String h5ImgUrl;

    public String getH5ImgUrl() {
        return h5ImgUrl;
    }

    public String getAndroidImgUrl() {
        return androidImgUrl;
    }

    public String getCode() {
        return code;
    }

    public String getGameKind() {
        return gameKind;
    }

    public String getUrl() {
        return url;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }
}
